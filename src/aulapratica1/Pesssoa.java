package aulapratica1;

public class Pesssoa {
    private int codigo;
    private String nome;

    public Pesssoa(int codigo, String nome) {
        this.codigo = codigo;
        this.nome = nome;
    }

    public int getCodigo() {
        return codigo;
    }

    public int getCodego() {
        return codigo;
    }

    public void  setCodigo(int codigo ) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void  setNome(String nome) {
        this.nome = nome;
    }
}
